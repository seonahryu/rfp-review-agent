def seed_tacit_knowledge(db_path: Path) -> int:
    conn = sqlite3.connect(db_path)
    try:
        conn.execute(
            """
            CREATE TABLE IF NOT EXISTS legal_tacit_knowledge (
                item_no TEXT NOT NULL,
                title TEXT NOT NULL,
                content TEXT NOT NULL,
                created_at TEXT DEFAULT CURRENT_TIMESTAMP
            )
            """
        )
        columns = [row[1] for row in conn.execute("PRAGMA table_info(legal_tacit_knowledge)")]
        if columns != ["item_no", "title", "content", "created_at"]:
            conn.execute("ALTER TABLE legal_tacit_knowledge RENAME TO legal_tacit_knowledge_old")
            conn.execute(
                """
                CREATE TABLE legal_tacit_knowledge (
                    item_no TEXT NOT NULL,
                    title TEXT NOT NULL,
                    content TEXT NOT NULL,
                    created_at TEXT DEFAULT CURRENT_TIMESTAMP
                )
                """
            )
            conn.execute("DROP TABLE legal_tacit_knowledge_old")
        conn.execute("DELETE FROM legal_tacit_knowledge")
        conn.executemany(
            """
            INSERT INTO legal_tacit_knowledge (item_no, title, content)
            VALUES (?, ?, ?)
            """,
            TACIT_KNOWLEDGE,
        )
        conn.commit()
        return len(TACIT_KNOWLEDGE)
    finally:
        conn.close()


def main() -> None:
    parser = argparse.ArgumentParser(description="Seed operational tacit knowledge into the legal RAG DB.")
    parser.add_argument("db", type=Path, nargs="?", default=Path("clean_legal_rag.db"))
    args = parser.parse_args()
    count = seed_tacit_knowledge(args.db)
    print(f"Seeded {count} tacit knowledge rows into {args.db}")


if __name__ == "__main__":
    main()
